CREATE TYPE billing_interval_t AS ENUM ('monthly', 'yearly');
CREATE TYPE plan_status_t AS ENUM ('active', 'canceled', 'past_due');
CREATE TYPE payment_status_t AS ENUM ('succeeded', 'failed', 'pending');
CREATE TYPE ai_request_type_t AS ENUM ('feedback', 'suggestion', 'rewrite');
CREATE TYPE document_type_t AS ENUM ('general', 'social_media_post', 'creative_writing', 'article', 'report', 'email');
CREATE TYPE document_tone_t AS ENUM ('formal', 'informal', 'neutral', 'friendly', 'professional');
CREATE TYPE ad_event_type_t AS ENUM ('impression', 'click');

CREATE TABLE user_account (
    user_account_id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    username VARCHAR(50) NOT NULL UNIQUE,
    email VARCHAR(50) NOT NULL UNIQUE,
    password_hash VARCHAR(255) NOT NULL,
    telephone VARCHAR(20) UNIQUE,
    created_date TIMESTAMP NOT NULL,
    last_login_date TIMESTAMP NOT NULL
);

CREATE TABLE plan (
    plan_id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    plan_name VARCHAR(50) NOT NULL UNIQUE,
    is_active BOOLEAN NOT NULL
);

CREATE TABLE plan_price (
    plan_price_id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    plan_id INT NOT NULL REFERENCES plan(plan_id),
    price DECIMAL(10, 2) NOT NULL,
        billing_interval billing_interval_t NOT NULL,
    currency_code VARCHAR(3) NOT NULL,
    effective_date TIMESTAMP NOT NULL,
    is_enabled BOOLEAN NOT NULL,

    CONSTRAINT price_positive CHECK (price >= 0)
);

CREATE TABLE feature (
    feature_id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    feature_name VARCHAR(50) NOT NULL UNIQUE,
    feature_description TEXT
);

CREATE TABLE plan_feature (
    plan_id INT NOT NULL REFERENCES plan(plan_id),
    feature_id INT NOT NULL REFERENCES feature(feature_id),
    is_enabled BOOLEAN NOT NULL,
    daily_limit INT NULL,
    monthly_limit INT NULL,

    PRIMARY KEY (plan_id, feature_id)
);

CREATE TABLE subscription (
    subscription_id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_account_id INT NOT NULL REFERENCES user_account(user_account_id),
    plan_price_id INT NOT NULL REFERENCES plan_price(plan_price_id),
    plan_start_date TIMESTAMP NOT NULL,
    plan_end_date TIMESTAMP NOT NULL,
    next_renewal TIMESTAMP NOT NULL,
    plan_status plan_status_t NOT NULL
);

CREATE TABLE payment (
    payment_id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    subscription_id INT NOT NULL REFERENCES subscription(subscription_id),
    amount DECIMAL(10, 2) NOT NULL,
    currency_code VARCHAR(3) NOT NULL,
    payment_date TIMESTAMP NOT NULL,
    payment_status payment_status_t NOT NULL,

    CONSTRAINT amount_positive CHECK (amount >= 0)
);

CREATE TABLE document_setting (
    document_setting_id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    document_type document_type_t NOT NULL,
    target_audience VARCHAR(50) NOT NULL,
    tone document_tone_t NOT NULL
);

CREATE TABLE document_metadata (
    document_metadata_id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_account_id INT NOT NULL REFERENCES user_account(user_account_id),
    document_setting_id INT NOT NULL REFERENCES document_setting(document_setting_id),
    created_date TIMESTAMP NOT NULL,
    last_modified_date TIMESTAMP NOT NULL,
    word_count INT,

    CONSTRAINT word_count_positive CHECK (word_count IS NULL OR word_count >= 0)
);

CREATE TABLE ai_request (
    ai_request_id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_account_id INT NOT NULL REFERENCES user_account(user_account_id),
    document_metadata_id INT NOT NULL REFERENCES document_metadata(document_metadata_id),
    request_type ai_request_type_t NOT NULL,
    context_length INT NOT NULL,
    request_time TIMESTAMP NOT NULL,

    CONSTRAINT context_length_positive CHECK (context_length >= 0)
);

CREATE TABLE ad_impression (
    ad_impression_id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_account_id INT NOT NULL REFERENCES user_account(user_account_id),
    event_type ad_event_type_t NOT NULL,
    impression_date TIMESTAMP NOT NULL
);
